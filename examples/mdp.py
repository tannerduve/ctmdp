from ctmdp.mdp import MDP

mdp_desc = {
    (0, 0): {
        "right": (
            {
                (1, 0): 1,
            },
            0.5,
        ),
        "left": ({(0, 0): 1}, 0.0),
        "up": ({(0, 1): 1}, 0.2),
        "down": ({(0, 0): 1}, -0.1),
        "right/up": ({(1, 0): 0.5, (0, 1): 0.5}, 0.3),
    },
    (1, 0): {
        "right": ({(1, 0): 1}, 0.5),
        "left": ({(0, 0): 1}, 0.0),
        "up": ({(1, 1): 1}, 0.2),
        "down": ({(1, 0): 1}, 0.1),
        "right/up": ({(1, 0): 0.5, (1, 1): 0.5}, 0.3),
    },
    (0, 1): {
        "right": ({(1, 1): 1}, 0.7),
        "left": ({(0, 1): 1}, 0.3),
        "up": ({(0, 1): 1}, 0.2),
        "down": ({(0, 0): 1}, 0.1),
        "right/down": ({(0, 0): 0.5, (1, 1): 0.5}, 0.4),
    },
    (1, 1): {
        "right": ({(1, 1): 1}, 0.7),
        "left": ({(0, 1): 1}, 0.3),
        "up": ({(1, 1): 1}, 0.2),
        "down": ({(1, 0): 1}, 0.1),
        "right/down": ({(0, 1): 0.5, (1, 0): 0.5}, 0.4),
    },
}

# Example usage
mdp = MDP(mdp_desc)
s = mdp.states[(0, 0)]
a1 = s.actions["right"]
a2 = s.actions["right/up"]
